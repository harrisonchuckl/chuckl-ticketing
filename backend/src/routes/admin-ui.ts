    function setActiveTab(target, page){
      document.querySelectorAll('.nav-tab').forEach(function(tab){
        tab.style.background = 'white';
        tab.style.color = '#4a5568';
        tab.style.border = '2px solid #e2e8f0';
      });
      if (target){
        target.style.background = '#009fe3';
        target.style.color = 'white';
        target.style.border = 'none';
      }

      var pages = ['overview', 'create', 'store', 'orders', 'settings', 'upsells'];
      pages.forEach(function(key){
        var el = document.getElementById(key + '-page');
        if (el) el.style.display = key === page ? 'block' : 'none';
      });
    }

